    private void btnPassChActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPassChActionPerformed

            nPass = tfNPass.getText();
            rPass = tfRPass.getText();
            if(nPass.equals(rPass)){
                try {
                    Statement s = c.connect.createStatement();
                    String sql = "update login set password = '"+nPass+"'where username = '"+username+"'";
                    
                    int Status = s.executeUpdate(sql);
                    
                    if(Status == 0){
                        JOptionPane.showMessageDialog(null,"Something went Wrong");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Password changed Succesfully");
                        setVisible(false);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Password do not match");
            }
    }//GEN-LAST:event_btnPassChActionPerformed

