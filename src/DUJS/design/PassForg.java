    private void SendOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendOTPActionPerformed

            Random rmd = new Random();
            long first4 = (rmd.nextLong() % 9000L) + 1000L;
            otp = "" +Math.abs(first4);

            Emto = tfEmail.getText();
            username = tfUserName.getText();

            EmailSec.setVisible(false);
            ConOTP.setVisible(true);

            Emcon = "Dear User "+username+",Your request for Changing Password has been Processed and this One Time Password is generated :\n"+otp+"\nEnter the OTP in the required Field/n/nREGARDS";
            Emsub = "Password Reset";

            SendMail.sendMail(Emto,Emsub,Emcon,otp);
    }//GEN-LAST:event_SendOTPActionPerformed

    private void btnConOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConOTPActionPerformed
        // TODO add your handling code here:
        if(evt.getSource()==btnConOTP){
            gotp = tfOTP.getText();
            if(gotp.equals(otp)){
                ConOTP.setVisible(false);
                NewPass.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect OTP,Please Check and Retry");
            }

        }
    }//GEN-LAST:event_btnConOTPActionPerformed

