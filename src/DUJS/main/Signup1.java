    private void tfMobileNoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfMobileNoKeyPressed
        String phNum = tfMobileNo.getText();
        
        int numLength = phNum.length();
        
        char c = evt.getKeyChar();
        
        if(evt.getKeyChar()>='0'&&evt.getKeyChar()<='9'){
            if(numLength<10){
                tfMobileNo.setEditable(true);
            }
            else{
                tfMobileNo.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE){
                tfMobileNo.setEditable(true);
            }
            else{
                tfMobileNo.setEditable(false);
            }
        }
    }//GEN-LAST:event_tfMobileNoKeyPressed

